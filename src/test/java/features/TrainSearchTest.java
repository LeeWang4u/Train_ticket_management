    public static void main(String[] args) {
        String[][] testCases = {
                {"H√† N·ªôi", "S√†i G√≤n", "2025-07-15"},
                {"", "", ""},
                {"H√† N·ªôi", "", ""},
                {"", "S√†i G√≤n", ""},
                {"H√† N·ªôi", "S√†i G√≤n", ""},
                {"ƒê√† N·∫µng", "Hu·∫ø", "2025-08-01"}
                // Th√™m c√°c case kh√°c n·∫øu c·∫ßn
        };

        for (String[] testCase : testCases) {
            String fromStation = testCase[0];
            String toStation = testCase[1];
            String tripDate = testCase[2];

            System.out.printf("üîπ Running test: from='%s', to='%s', date='%s'%n", fromStation, toStation, tripDate);
            runTrainSearchTest(fromStation, toStation, tripDate);
        }
    }


    public static void runTrainSearchTest(String fromStation, String toStation, String tripDate) {
        ChromeOptions options = new ChromeOptions();
        options.addArguments("--remote-allow-origins=*");
        options.setExperimentalOption("excludeSwitches", new String[]{"enable-automation"});
        options.setExperimentalOption("useAutomationExtension", false);
        WebDriver driver = new ChromeDriver(options);

        try {
            WebDriverWait wait = new WebDriverWait(driver, Duration.ofSeconds(10));

            // M·ªü trang ch√≠nh
            driver.get("http://localhost:5173");

            // Nh·∫≠p ga ƒëi
            WebElement fromInput = wait.until(ExpectedConditions.visibilityOfElementLocated(
                    By.xpath("//label[contains(text(),'Ga ƒëi')]/following-sibling::input")
            ));
            fromInput.sendKeys(fromStation);

            // Nh·∫≠p ga ƒë·∫øn
            WebElement toInput = driver.findElement(
                    By.xpath("//label[contains(text(),'Ga ƒë·∫øn')]/following-sibling::input")
            );
            toInput.sendKeys(toStation);

            // Nh·∫≠p ng√†y ƒëi
            WebElement dateInput = driver.findElement(By.cssSelector("input[type='date']"));
            dateInput.sendKeys(tripDate);  // ƒê·ªãnh d·∫°ng: yyyy-MM-dd

            // Click n√∫t t√¨m ki·∫øm
            WebElement searchButton = driver.findElement(
                    By.xpath("//button[contains(text(),'T√¨m ki·∫øm')]")
            );
            searchButton.click();

            // Ch·ªù chuy·ªÉn sang trang booking
            wait.until(ExpectedConditions.urlContains("/booking"));

            // Ch·ªù ti√™u ƒë·ªÅ trang booking xu·∫•t hi·ªán
            wait.until(ExpectedConditions.visibilityOfElementLocated(By.id("booking-title")));

            System.out.println("‚úÖ Test passed: Successfully navigated to booking page.");

            Thread.sleep(3000);  // T·∫°m d·ª´ng quan s√°t

        } catch (Exception e) {
            System.out.println("‚ùå Test failed: " + e.getMessage());
        } finally {
            driver.quit();
        }
    }
